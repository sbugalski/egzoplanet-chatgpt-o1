import streamlit as st

st.set_page_config(page_title="Czym są egzoplanety?")

st.title("Czym są egzoplanety?")
st.markdown(
    """
**Egzoplanety** (planety pozasłoneczne) to planety, które krążą wokół gwiazd innych niż Słońce.
Pierwsza potwierdzona egzoplaneta została odkryta w 1992 roku. Od tamtej pory, dzięki postępowi w technikach obserwacyjnych, takich jak metoda tranzytów czy pomiar prędkości radialnej gwiazd, odkryliśmy już tysiące egzoplanet o zróżnicowanych rozmiarach, masach i charakterystykach.

Kluczowe fakty o egzoplanetach:
- **Różnorodność**: Spotykamy egzoplanety od gorących "Jowiszów" krążących bardzo blisko swoich gwiazd po skaliste światy podobne do Ziemi, a nawet planety swobodne, niezwiązane z żadną gwiazdą.
- **Strefa zamieszkiwalna**: Szczególnie interesujące są planety znajdujące się w tzw. strefie zamieszkiwalnej, gdzie temperatura może sprzyjać istnieniu ciekłej wody na powierzchni, a tym samym potencjalnemu powstaniu i utrzymaniu życia.
- **Techniki odkryć**: Najpopularniejsze metody to:
  - **Metoda tranzytów**: Pomiar spadku jasności gwiazdy, gdy egzoplaneta przechodzi przed jej tarczą z punktu widzenia obserwatora.
  - **Metoda prędkości radialnych**: Wykrywanie subtelnych wahań w ruchu gwiazdy spowodowanych grawitacyjnym wpływem krążącej wokół niej planety.

Dalszy rozwój instrumentów astronomicznych, teleskopów kosmicznych (jak Teleskop Jamesa Webba) oraz technik analizy danych pozwala nam coraz lepiej zrozumieć te odległe światy i być może pewnego dnia odkryć planety z warunkami sprzyjającymi życiu.
"""
)
